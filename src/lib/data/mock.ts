function buildDate(daysAgo: number, hour = 8) {
  const date = new Date();
  date.setDate(date.getDate() - daysAgo);
  date.setHours(hour, 0, 0, 0);
  return date;
}

function makeUser(
  id: string,
  nickname: string,
  email: string,
  avatarColor: SourceUser["avatarColor"],
): SourceUser {
  return {
    id,
    nickname,
    email,
    avatarColor,
    createdAt: buildDate(90).toISOString(),
  };
}

function makeLog(
  id: string,
  groupId: string,
  userId: string,
  bookId: string | null,
  daysAgo: number,
  pagesRead: number,
  memo: string,
  didRead = true,
  hour = 8,
  readingTime: number | null = null,
): SourceReadingLog {
  const date = buildDate(daysAgo, hour);

  return {
    id,
    groupId,
    userId,
    bookId,
    date: toDateKey(date),
    dayOfWeek: date.getDay(),
    didRead,
    pagesRead,
    memo,
    readingTime,
    startPage: didRead && pagesRead > 0 ? 40 : null,
    endPage: didRead && pagesRead > 0 ? 40 + pagesRead : null,
    moodTag: didRead ? "집중" : null,
    createdAt: date.toISOString(),
  };
}

